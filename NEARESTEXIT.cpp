#include <bits/stdc++.h>

using namespace std;

int main()
{
    int x;
    cin >> x;
    while (x--)
    {

        int g;
        cin >> g;

        if (g < 51)
        {
            cout << "LEFT" << endl;
        }
        else
        {
            cout << "RIGHT" << endl;
        }
    }
    return 0;
}